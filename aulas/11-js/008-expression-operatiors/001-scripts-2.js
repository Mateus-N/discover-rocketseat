console.log ( 10 * 5 )
console.log ( 10 / 5 )
console.log ( 10 + 5 )
console.log ( 10 - 5 )

// remainder = resto
console.log ( 10 % 8 )

// increment
let increment = 0
increment++
// ++increment na linha atual - increment++ na linha seguinte
console.log ( increment )

// decrement
let decrement = 0
// --decrement na linha atual - decrement-- na linha seguinte
decrement--
console.log ( decrement )

// exponencial
console.log ( 2 ** 3 )